public class AdConfig {

    private String adInster;
    private String adSplash;
    private String adVideo;
    private String adBanner;
    private String appName;

    public String getAdInster() {
        return adInster;
    }

    public void setAdInster(String adInster) {
        this.adInster = adInster;
    }

    public String getAdSplash() {
        return adSplash;
    }

    public void setAdSplash(String adSplash) {
        this.adSplash = adSplash;
    }

    public String getAdVideo() {
        return adVideo;
    }

    public void setAdVideo(String adVideo) {
        this.adVideo = adVideo;
    }

    public String getAdBanner() {
        return adBanner;
    }

    public void setAdBanner(String adBanner) {
        this.adBanner = adBanner;
    }

    public String getAppName() {
        return appName;
    }

    public void setAppName(String appName) {
        this.appName = appName;
    }
}
